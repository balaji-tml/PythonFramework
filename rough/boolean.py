"""
Examples to show how boolean works in python
"""

a = True
b = False

print('a:',a)
print('b:',b)

print("**************************")
print('bool(0):',bool(0))
print('bool(1):',bool(1))
print('bool(2):',bool(2))

c = ""
print('bool("',c,'"):',bool(c))

c = "Some Value"
print('bool("',c,'"):',bool(c))