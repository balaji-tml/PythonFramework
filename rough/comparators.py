"""
== --> Value Equality
!= --> Not equal to
< --> Less than
> --> Greater than
<= --> Less than or equal to
>= --> Greater than or equal to
"""

bool_one = 10 == 11
print(bool_one)     #false
not_equal = 10 != 11
print(not_equal)   #true
less_than = 10 < 11
print(less_than)  #true
greater_than = 10 > 9
print(greater_than)   #true
lt_eq = 10 <= 10
print(lt_eq)   #true
gt_eq = 10 >= 11 - 1
print(gt_eq)   #true
print (gt_eq)